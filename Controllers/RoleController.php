     */
    public function store(Request $request)
    {
        if($request->has('name') && $request->name) {
            Role::create(['name' => $request->name]);
            return response()->json(['message' => 'Role '.$request->name.' created']);
        }
        
        return response()->json(['message' => 'Invalid string'], 300);
